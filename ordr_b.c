#include "push_swap.h"

int	ordr_b(long long int **tab, t_pushy *swap)
{
	int	x;

	x = 0;
	while (x < swap->sizea)
	{
		if (tab[1][x] < tab[1][x + 1])
			return (-1);
		x++;
	}
	return (1);
}
