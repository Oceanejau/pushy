#include "push_swap.h"

int	check_arg(int c, char **v, int x)
{
	int	nb;

	nb = 0;
	if (c > 2)
		return (-1);
	if (v[1][x] != '-' && v[1][x] != '+' && (v[1][x] < '0' || v[1][x] > '9'))
		return (-1);
	while (v[1][x] != '\0')
	{
		if (v[1][x] == ' ')
			x++;
		if ((v[1][x] == '-' || v[1][x] == '+') && (v[1][x + 1] >= '0' &&
					v[1][x + 1] <= '9'))
			x++;
		if (v[1][x] >= '0' && v[1][x] <= '9')
		{
			while (v[1][x] >= '0' && v[1][x] <= '9')
				x++;
			nb++;
		}
		if ((v[1][x] != ' ' && v[1][x] != '\0') || (v[1][x] == ' ' &&
					(v[1][x + 1] == ' ' || v[1][x + 1] == '\0')))
			return (-1);
	}
	return (nb);
}
