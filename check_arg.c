#include "push_swap.h"

int	check_arg(char **v, int y)
{
	int	x;
	int	ret;
	
	x = 0;
	ret = 0;
	if (v[y][x] != '-' && v[y][x] != '+')
		x++;
	while (v[y][x] != '\0' && ret != -1)
	{
		while (v[y][x] >= '0' && v[y][x] <= '9')
				x++;
		if (v[y][x] <= '0' || v[y][x] >= '9')
			ret = -1;
	}
	return (ret);
}
